    fn render_package_json(&self, deps: &[Dependency]) -> DependencyFile {
        let runtime: Vec<&Dependency> = deps.iter().filter(|d| !d.dev).collect();
        let dev: Vec<&Dependency> = deps.iter().filter(|d| d.dev).collect();

        let runtime_lines: Vec<String> = runtime.iter().map(|d| {
            format!("    \"{}\": \"{}\"", d.name, d.version)
        }).collect();
        let dev_lines: Vec<String> = dev.iter().map(|d| {
            format!("    \"{}\": \"{}\"", d.name, d.version)
        }).collect();

        let content = format!(
            "{{\n  \"name\": \"generated\",\n  \"version\": \"0.1.0\",\n  \"type\": \"module\",\n  \"scripts\": {{\n    \"dev\": \"vite\",\n    \"build\": \"tsc && vite build\",\n    \"preview\": \"vite preview\",\n    \"test\": \"vitest\"\n  }},\n  \"dependencies\": {{\n{}\n  }},\n  \"devDependencies\": {{\n{}\n  }}\n}}",
            runtime_lines.join(",\n"),
            dev_lines.join(",\n")
        );

        DependencyFile { filename: "package.json", content }
    }

    // ── Python ───────────────────────────────────────────────

    fn resolve_python(&self, framework: Option<&str>) -> Result<Vec<DependencyFile>> {
        let deps = match framework {
            Some("fastapi") => self.py_fastapi_deps(),
            Some("django") => self.py_django_deps(),
            Some("flask") => self.py_flask_deps(),
            _ => self.py_fastapi_deps(),
        };

        let mut files = Vec::new();

        let req_lines: Vec<String> = deps.iter()
            .filter(|d| !d.dev)
            .map(|d| format!("{}=={}", d.name, d.version))
            .collect();

        files.push(DependencyFile {
            filename: "requirements.txt",
            content: format!("# Generated by ACoder\n# Runtime dependencies\n{}\n", req_lines.join("\n")),
        });

        files.push(DependencyFile {
            filename: "pyproject.toml",
            content: "[project]\nname = \"generated\"\nversion = \"0.1.0\"\nrequires-python = \">=3.10\"\ndependencies = []\n\n[tool.pytest.ini_options]\ntestpaths = [\"tests\"]\n\n[tool.black]\nline-length = 100\n".into(),
        });

        files.push(DependencyFile {
            filename: ".gitignore",
            content: "__pycache__\n*.pyc\n.venv\n.env\n*.egg-info\ndist\nbuild\n".into(),
        });

        Ok(files)
    }

