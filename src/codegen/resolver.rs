    fn render_cargo_toml(&self, deps: &[Dependency]) -> DependencyFile {
        let mut runtime: Vec<&Dependency> = deps.iter().filter(|d| !d.dev).collect();
        let dev_deps: Vec<&Dependency> = deps.iter().filter(|d| d.dev).collect();

        let mut lines = vec![
            "# Generated by ACoder".into(),
            "[package]".into(),
            "name = \"generated\"".into(),
            "version = \"0.1.0\"".into(),
            "edition = \"2021\"".into(),
            "".into(),
            "[lib]".into(),
        ];

        if !runtime.is_empty() {
            lines.push("".into());
            lines.push("[dependencies]".into());
            for dep in runtime {
                lines.push(format!("{} = \"{}\"", dep.name, dep.version));
            }
        }

        if !dev_deps.is_empty() {
            lines.push("".into());
            lines.push("[dev-dependencies]".into());
            for dep in dev_deps {
                lines.push(format!("{} = \"{}\"", dep.name, dep.version));
            }
        }

        lines.push("".into());
        lines.push("[features]".into());
        lines.push("default = []".into());

        DependencyFile {
            filename: "Cargo.toml",
            content: lines.join("\n"),
        }
    }

    // ── TypeScript ────────────────────────────────────────────

    fn resolve_typescript(&self, framework: Option<&str>) -> Result<Vec<DependencyFile>> {
        let mut files = Vec::new();
        let deps = match framework {
            Some("react") | Some("nextjs") => self.ts_react_deps(),
            Some("vue") | Some("nuxt") => self.ts_vue_deps(),
            Some("express") => self.ts_express_deps(),
            _ => self.ts_react_deps(),
        };

        files.push(self.render_package_json(&deps));

        files.push(DependencyFile {
            filename: "vite.config.ts",
            content: "import { defineConfig } from 'vite'\nimport react from '@vitejs/plugin-react'\n\nexport default defineConfig({\n  plugins: [react()],\n  server: { port: 3000 },\n})\n".into(),
        });

        files.push(DependencyFile {
            filename: "tsconfig.json",
            content: "{
  \"compilerOptions\": {
    \"target\": \"ES2020\",
    \"lib\": [\"ES2020\", \"DOM\", \"DOM.Iterable\"],
    \"module\": \"ESNext\",
    \"skipLibCheck\": true,
    \"moduleResolution\": \"bundler\",
    \"allowImportingTsExtensions\": true,
    \"resolveJsonModule\": true,
    \"isolatedModules\": true,
    \"noEmit\": true,
    \"jsx\": \"react-jsx\",
    \"strict\": true,
    \"noUnusedLocals\": true,
    \"noFallthroughCasesInSwitch\": true
  },
  \"include\": [\"src\"]
}
".into(),
        });

        files.push(DependencyFile {
            filename: ".gitignore",
            content: "node_modules\ndist\n.env\n*.log\n".into(),
        });

        Ok(files)
    }

